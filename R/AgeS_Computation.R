  }

  # Create return objecty -------------------------------------------------------------------------
  output <- .list_BayLum(
    "Ages" = data.frame(
      SAMPLE = SampleNames,
      AGE = AgePlotMoy,
      HPD68.MIN = AgePlot68[,2],
      HPD68.MAX = AgePlot68[,3],
      HPD95.MIN = AgePlot95[,2],
